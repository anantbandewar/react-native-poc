class Address {
    constructor(addressId, city, cityId, country, postalCode, state, type) {
        this.addressId = addressId;
        this.city = city;
        this.cityId = cityId;
        this.country = country;
        this.postalCode = postalCode;
        this.state = state;
        this.type = type;
    }
}

export default Address;
